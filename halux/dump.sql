-- Create tables
CREATE TABLE IF NOT EXISTS TEMPERATURE (
    temperature_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

CREATE TABLE IF NOT EXISTS HUMIDITY (
    humidity_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

CREATE TABLE IF NOT EXISTS IR_STATE (
    ir_state_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

CREATE TABLE IF NOT EXISTS PERMISSION_STATE (
    permission_state_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

CREATE TABLE IF NOT EXISTS FREQUENCY (
    frequency_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

CREATE TABLE IF NOT EXISTS PASSWORD (
    password_id INTEGER PRIMARY KEY AUTOINCREMENT,
    lastChanged TEXT,
    value TEXT
);

